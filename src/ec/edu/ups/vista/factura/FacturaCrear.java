    public void calcularSubtotal() {
        double sub = 0;
        for (int i = 0; i < factura.getDetalle().size(); i++) {
            sub += factura.getDetalle().get(i).getTotalCP();
        }
        factura.setSubtotal(sub);
    }

    public void calcularTotal() {
        double tot = factura.getSubtotal() + factura.getIva();
        factura.setTotal(tot);
    }

