    private void bmActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bmActionPerformed
        // TODO add your handling code here:
        Veterinario veterinario = controladorveterinario.read(Integer.parseInt(txtcodigo.getText()));
        veterinario.setNombre(txtnombre.getText());
        veterinario.setApellido(txtapellido.getText());
        veterinario.setCedula(txtcedula.getText());
        veterinario.setEdad(Integer.parseInt(txtedad.getText()));
        veterinario.setSueldo(Double.valueOf(txtsueldo.getText()));
        veterinario.setTitulo(txttitulo.getText());
        controladorveterinario.update(veterinario);
        JOptionPane.showMessageDialog(this, "Veterinario actualizado exitosamente!!", "veterinario cliente", JOptionPane.OK_OPTION);
        txtcodigo.setText("");
        txtcedula.setText("");
        txtnombre.setText("");
        txtapellido.setText("");
        txtedad.setText("");
        txtsueldo.setText("");
        txttitulo.setText("");
    }//GEN-LAST:event_bmActionPerformed

    private void bbActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bbActionPerformed
        // TODO add your handling code here:
        String cedula = txtcedula.getText();
        Veterinario buscarv = controladorveterinario.readCedula(cedula);
        txtnombre.setText(buscarv.getNombre());
        txtapellido.setText(buscarv.getApellido());
        txtcodigo.setText(String.valueOf(buscarv.getCodigo()));
        txtedad.setText(String.valueOf(buscarv.getEdad()));
        txtsueldo.setText(String.valueOf(buscarv.getSueldo()));
        txttitulo.setText(buscarv.getTitulo());
    }//GEN-LAST:event_bbActionPerformed

